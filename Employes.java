public class Employes{
    protected String nom_employe_bibliotheque;
    protected String prenom_employe_bibliotheque;
    protected String fonction_employe_bibliotheque;    
    protected int salaire_employe_bibliotheque;

    public Employes(String nom_employe, String prenom_employe, String fonction_employe, int salaire_employe){
        nom_employe = nom_employe_bibliotheque;
        prenom_employe = prenom_employe_bibliotheque;
        fonction_employe = fonction_employe_bibliotheque;
        salaire_employe = salaire_employe_bibliotheque;
    }

}